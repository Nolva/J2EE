package com.test.entity;

public class Manager {

    private String managerId;
    private String manPassword;
    private String manName;
    private String manTelephone;
    private String manEmail;
    private int manPer;

    public String getManagerId() {
        return managerId;
    }

    public void setManagerId(String managerId) {
        this.managerId = managerId;
    }

    public String getManPassword() {
        return manPassword;
    }

    public void setManPassword(String manPassword) {
        this.manPassword = manPassword;
    }

    public String getManName() {
        return manName;
    }

    public void setManName(String manName) {
        this.manName = manName;
    }

    public String getManTelephone() {
        return manTelephone;
    }

    public void setManTelephone(String manTelephone) {
        this.manTelephone = manTelephone;
    }

    public String getManEmail() {
        return manEmail;
    }

    public void setManEmail(String manEmail) {
        this.manEmail = manEmail;
    }

    public int getManPer() {
        return manPer;
    }

    public void setManPer(int manPer) {
        this.manPer = manPer;
    }
}